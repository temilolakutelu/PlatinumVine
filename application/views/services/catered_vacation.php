                             owned and operated by Concierge Services, Inc. Set amidst sixty-five
                             rolling acres of meadows, ponds and streams, Windsor Farm seems almost sunken,
                              as the Green Mountains rise around it in every direction giving guests the sense of being cradled by Mother Earth herself. We invite you to spend a long weekend with us—or however long you wish—enjoying fresh meals prepared especially for you by our on-site chef using ingredients from our very own farm. After lunch, take a stroll to the gazebo overlooking our two-acre pond where you may wish to take a dip in the mid-summer heat. Once summer finally welcomes Autumn, our horse-drawn carriages await to escort you through wood-covered bridges just down the country lane from our farm. You’ll enjoy the views of impeccably kept properties and lands along the way, while your olfactory senses are bedazzled by the mixture of burnt leaves and apple cider filling the crisp New England air. Do not worry. Heated blankets will accompany you on your little journey, and when you return home your concierge will be standing by, arranging activities from cheese and craft beer tasting sessions to maple syrup making in March. No trip to Vermont would be complete without mentioning ski country. The Vermont ski industry offers a range of services catering to ski aficionados of every level of expertise: Killington; Jay Peak; Smugglers Notch; Sugarbush Resort and of course Stowe, renown to many as the finest ski destination on the East Coast. You decide.
