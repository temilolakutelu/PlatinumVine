                        <h1 class="title text-center">Our Inspiration</h1>
                        <p>
                            In 2006, Concierge Services, Inc. was created to raise the standard of service to residents of luxury properties throughout Greater Boston. Before establishing the company, Owner Greg Doyle himself worked as a concierge for a private residential community in Brookline, MA. While Greg believed service was adequate in terms of staffing, he felt service overall was dated and not reflective of how residents and guests live today, in a technologically advanced society. The market was in need of a company that incorporated technology into its operations
                            while staying true to the precepts of traditional hospitality service.
