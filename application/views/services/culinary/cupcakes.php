                                   <p>In 2010 mother-daughter team Tracey Noonan and Danielle Vilagie took cake-decorating classes as a way to spend quality time together. Their passion quickly grew into a business
                                            and they launched their first store in October of 2011 In Cohasset, Massachusetts.
                                        </p>
                                        <p>As the reputation of their product grew, they found themselves getting hundreds of requests to ship their product across the country. However, they couldn’t find a way to effectively ship cupcakes and have them arrive fresh and intact. That's when they came up for the idea of putting cupcakes into mason jars. By filling jars with freshly baked layers of cake, frosting and filling, they were able to create a product that would remain
                                            fresh up to 10 days without refrigeration and that could be easily shipped.
                                        </p>
                                        <p>News of this innovative product began to spread. In April of 2013, Tracey and Dani debuted their product in front of 10 million television viewers on ABC's hit reality show, Shark Tank. They attracted the attention of “Mr Wonderful”, Kevin O'Leary and subsequently struck a deal
                                            that Kevin has since called, “the best investment” he's ever made on the show.
                                        </p>
                                        <p>Tracey and Dani have not looked back. Since their appearance on Shark Tank, Wicked Good Cupcakes has become the "go to" website to buy unique and gourmet baked goods online. They continue to grow and innovate, constantly creating new products for their customers. To this day, everything they do is still homemade and baked from scratch
                                            with absolutely no mixes. Tracey and Dani wouldn't have it any other way.
                                        </p>
